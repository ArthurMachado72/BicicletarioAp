    private void btnAdicionar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionar1ActionPerformed
        // Declaração e inicialização dos serviços
        ClienteService clienteService = new ClienteService();
        BicicletaService bicicletaService = new BicicletaService();
        
        int selectedRow = jTable5.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Selecione uma linha vazia para adicionar um aluguel.Deixe a coluna 'id' vazia!", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Verifica se a linha selecionada está vazia
        if (tableModel.getValueAt(selectedRow, 0) != null) {
            JOptionPane.showMessageDialog(this, "Selecione uma linha vazia para adicionar um aluguel.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Coleta os dados da linha selecionada
        Integer clienteId = (Integer) tableModel.getValueAt(selectedRow, 1); // Coluna "cliente_id"
        Integer bicicletaId = (Integer) tableModel.getValueAt(selectedRow, 2); // Coluna "bicicleta_id"
        String horaInicio = (String) tableModel.getValueAt(selectedRow, 3); // Coluna "hora_inicio"
        String horaTermino = (String) tableModel.getValueAt(selectedRow, 4); // Coluna "hora_termino"
        Double precoHora = (Double) tableModel.getValueAt(selectedRow, 5); // Coluna "preco_hora"

        // Verifica se todos os campos foram preenchidos
        if (clienteId == null || bicicletaId == null || horaInicio == null || horaTermino == null || precoHora == null) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos da linha selecionada.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            // Cria um novo objeto Aluguel
            Aluguel aluguel = new Aluguel();

            // Busca o cliente no banco de dados
            Cliente cliente = clienteService.buscarClientePorId(clienteId);
            if (cliente == null) {
                JOptionPane.showMessageDialog(this, "Cliente não encontrado.", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }
            aluguel.setCliente(cliente);

            // Busca a bicicleta no banco de dados
            Bicicleta bicicleta = bicicletaService.buscarBicicletaPorId(bicicletaId);
            if (bicicleta == null) {
                JOptionPane.showMessageDialog(this, "Bicicleta não encontrada.", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }
            aluguel.setBicicleta(bicicleta);

            // Converte as strings para Date
            SimpleDateFormat formatoHora = new SimpleDateFormat("HH:mm");
            Date horaInicioDate = formatoHora.parse(horaInicio);
            Date horaTerminoDate = formatoHora.parse(horaTermino);

            // Define os campos de data e hora
            aluguel.setHora_inicio(horaInicioDate);
            aluguel.setHora_termino(horaTerminoDate);

            // Define o preço por hora
            aluguel.setPreco_hora(precoHora);

            // Salva o aluguel no banco de dados
            aluguelService.salvarAluguel(aluguel);

            // Atualiza a tabela com os dados do banco de dados
            carregarAlugueis();

            JOptionPane.showMessageDialog(this, "Aluguel adicionado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Formato de hora inválido. Use o formato HH:mm.", "Erro", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao adicionar aluguel: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnAdicionar1ActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int selectedRow = jTable5.getSelectedRow(); // Usa jTable5 em vez de jTable3
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um aluguel para excluir.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int aluguelId = (int) tableModel.getValueAt(selectedRow, 0);
        int confirmacao = JOptionPane.showConfirmDialog(
                this,
                "Tem certeza que deseja excluir o aluguel com ID " + aluguelId + "?",
                "Confirmar Exclusão",
                JOptionPane.YES_NO_OPTION
        );

        if (confirmacao == JOptionPane.YES_OPTION) {
            aluguelService.excluirAluguel(aluguelId);
            carregarAlugueis(); // Atualiza a tabela após a exclusão
            JOptionPane.showMessageDialog(this, "Aluguel excluído com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

