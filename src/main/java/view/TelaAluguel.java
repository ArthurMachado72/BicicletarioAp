    private void carregarAlugueis() {
        tableModel.setRowCount(0);
        List<Aluguel> alugueis = aluguelService.listarAlugueis(); // Obtém a lista de aluguéis
        for (Aluguel aluguel : alugueis) {
            tableModel.addRow(new Object[]{
                aluguel.getAluguel_id(),
                aluguel.getCliente().getCliente_id(), // Usa getCliente_id() em vez de getId()
                aluguel.getBicicleta().getBicicleta_id(), // Usa getBicicleta_id() em vez de getId()
                new SimpleDateFormat("HH:mm").format(aluguel.getHora_inicio()),
                new SimpleDateFormat("HH:mm").format(aluguel.getHora_termino()),
                aluguel.getPreco_hora()
            });
        }
        // Adiciona 5 linhas vazias para novos aluguéis
        for (int i = 0; i < 5; i++) {
            tableModel.addRow(new Object[]{null, null, null, null, null, null});
        }

    }

